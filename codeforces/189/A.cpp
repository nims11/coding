using namespace std;
int mod = 1000000007;
long long POW(long long r, long long n)
{
    int ans = 1;
    while(n>0)
    {
        if(n&1)
            ans = (ans*r)%mod;
        n >>= 1;
        r = (r*r)%mod;
    }
    return ans;
}
int main()
{
    string x;
    cin>>x;
    int n = x.length();
    long long ans = 0;
    for(int i=n-1;i>=0;i--)
    {
        if(x[i] == '1')
        {
            ans = (ans + POW(2, i)*POW(2, 2*(n-i-1)))%mod;
        }
    }
    cout<<ans<<endl;
}
